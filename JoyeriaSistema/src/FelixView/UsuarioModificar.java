    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try {         
            contr.llenarComboBoxH(cbxHorario);
            contr.llenarComboBoxP(cbxPuesto);
            contr.llenarComboBoxS(cbxSucursal);
            txtIdU.setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(UsuarioAgregar.class.getName()).log(Level.SEVERE, null, ex);
        }

            txtIdU.setVisible(false);
            txtNombre.setText(um.getNombre());
            txtApellidoM.setText(um.getApellidoMaterno());
            txtApellidoP.setText(um.getApellidoPaterno());
            txtDetalles.setText(um.getDetalles());
            txtDireccion.setText(um.getDireccion());
            txtIdU.setText(String.valueOf(um.getId_vendedor()));
            txtFechaIng.setText(um.getFechaIngreso());
            txtTelefono.setText(um.getTelefono());
            txtDetalles.setText(um.getDetalles());
            txtFechaIng.setText(um.getFechaIngreso());
            

    }//GEN-LAST:event_formComponentShown

