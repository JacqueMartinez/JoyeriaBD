    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        LoginDAO modSql= new LoginDAO();
        Usuario mod= new Usuario();
        String us= txtUsuario.getText();
        String pass= new String(txtPass.getPassword());
        if(!txtUsuario.getText().equals("") && !pass.equals("")){
            mod.setNombre(txtUsuario.getText());
            mod.setPass(pass);
            if(cbNivel.getSelectedItem()=="Administrador"){
                if(modSql.loginA(mod)){
                    this.dispose();
                    V_Inicio inicio=new V_Inicio(mod);
                    inicio.setVisible(true);
                    
                
                }else{
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
                }
            }else if(cbNivel.getSelectedItem()=="Gerente"){
                if(modSql.loginG(mod)){
                this.dispose();
                V_Inicio inicio=new V_Inicio(mod);
                inicio.setVisible(true);
                
                }else{
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
                }
                
            }else if(cbNivel.getSelectedItem()=="Vendedor"){
                if(modSql.loginV(mod)){
                this.dispose();
                V_Inicio inicio=new V_Inicio(mod);
                inicio.setVisible(true);
                
                }else{
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
                }
            }
            
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe ingresar sus datos");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

